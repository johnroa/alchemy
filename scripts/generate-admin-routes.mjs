#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import url from "node:url";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const routesDir = path.join(repoRoot, "apps/admin/app/api/admin");
const outputFile = path.join(repoRoot, "apps/admin/lib/admin-routes.ts");
const methods = ["GET", "POST", "PATCH", "PUT", "DELETE"];

const walk = (dir, out = []) => {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath, out);
      continue;
    }
    if (entry.isFile() && entry.name === "route.ts") {
      out.push(fullPath);
    }
  }
  return out;
};

export const collectAdminRoutes = () => {
  const files = walk(routesDir).sort();
  const routes = [];

  for (const file of files) {
    const source = fs.readFileSync(file, "utf8");
    const relativeDir = path
      .relative(path.join(repoRoot, "apps/admin/app"), path.dirname(file))
      .replace(/\\/g, "/");
    const routePath = `/${relativeDir}`.replace(/\[(.+?)\]/g, ":$1");

    for (const method of methods) {
      const pattern = new RegExp(`export\\s+async\\s+function\\s+${method}\\s*\\(`);
      if (pattern.test(source)) {
        routes.push({ method, path: routePath });
      }
    }
  }

  return routes.sort((left, right) => {
    const pathCompare = left.path.localeCompare(right.path);
    if (pathCompare !== 0) {
      return pathCompare;
    }
    return left.method.localeCompare(right.method);
  });
};

export const buildAdminRoutesSource = (routes) => {
  const rows = routes
    .map((route) => `  { path: ${JSON.stringify(route.path)}, method: ${JSON.stringify(route.method)} },`)
    .join("\n");

  return `/**
 * Generated by scripts/generate-admin-routes.mjs.
 * Source of truth: apps/admin/app/api/admin route handlers
 */
export const ADMIN_ROUTES: Array<{ path: string; method: string }> = [
${rows}
];
`;
};

const run = () => {
  const checkMode = process.argv.includes("--check");
  const routes = collectAdminRoutes();
  const nextSource = buildAdminRoutesSource(routes);
  const currentSource = fs.existsSync(outputFile) ? fs.readFileSync(outputFile, "utf8") : "";

  if (checkMode) {
    if (currentSource !== nextSource) {
      console.error("[admin-routes] Generated route inventory is stale. Run: node scripts/generate-admin-routes.mjs");
      process.exit(1);
    }
    console.log("[admin-routes] ok");
    return;
  }

  fs.writeFileSync(outputFile, nextSource);
  console.log(`[admin-routes] wrote ${path.relative(repoRoot, outputFile)}`);
};

if (import.meta.url === url.pathToFileURL(process.argv[1]).href) {
  run();
}
