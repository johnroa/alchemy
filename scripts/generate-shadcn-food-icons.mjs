#!/usr/bin/env node

import { writeFile } from "node:fs/promises";
import path from "node:path";

const FOOD_PAGE_COUNT = 60;
const FOOD_TAG_URL = "https://www.shadcn.io/icons/tag/food";
const DETAIL_BASE = "https://www.shadcn.io/icon/";
const DEFAULT_OUTPUT = path.resolve(
  process.cwd(),
  "apps/admin/components/admin/generated/shadcn-food-icons.generated.ts"
);
const DEFAULT_CATALOG_OUTPUT = path.resolve(
  process.cwd(),
  "packages/shared/src/shadcn-food-icon-catalog.ts"
);
const DEFAULT_INGREDIENT_INDEX_OUTPUT = path.resolve(
  process.cwd(),
  "packages/shared/src/ingredient-semantic-icon-index.ts"
);

const LIBRARY_PRIORITY = ["openmoji", "noto", "twemoji", "fluent-emoji-flat"];
const LIBRARY_PRIORITY_INDEX = new Map(
  LIBRARY_PRIORITY.map((library, index) => [library, index])
);

const EXCLUDED_NAME_PATTERNS = [];

const ICON_STOP_TOKENS = new Set([
  "emoji",
  "food",
  "beverage",
  "beverages",
  "with",
  "without",
  "and",
  "of",
  "the",
  "flat",
  "outline",
  "outlined",
  "rounded",
  "sharp",
  "fill",
  "filled",
  "color",
  "icon",
  "icons",
  "v1",
  "v2"
]);

const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

const fetchText = async (url) => {
  const response = await fetch(url, {
    headers: {
      "user-agent": "alchemy-icon-generator/1.0",
      accept: "text/html,application/xhtml+xml"
    }
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }

  return response.text();
};

const parseFoodIconIds = (html) => {
  const ids = [];
  const pattern = /\\"l\\":\\"([a-z0-9-]+)\\",\\"n\\":\\"([a-z0-9-]+)\\"/g;
  let match;
  while ((match = pattern.exec(html)) !== null) {
    ids.push({
      id: `${match[1]}-${match[2]}`,
      library: match[1],
      name: match[2]
    });
  }
  return ids;
};

const extractInlineSvg = (html) => {
  const match = html.match(/src="data:image\/svg\+xml;base64,([^"]+)"/);
  if (!match) {
    return null;
  }

  const svg = Buffer.from(match[1], "base64").toString("utf8");
  return svg
    .replace(/\swidth="[^"]+"/g, "")
    .replace(/\sheight="[^"]+"/g, "")
    .trim();
};

const buildOutput = (entries) => {
  const iconArray = entries
    .map((entry) => {
      return `  ${JSON.stringify(entry.iconId)}: ${JSON.stringify(entry.svg)}`;
    })
    .join(",\n");

  const generatedAt = new Date().toISOString();

  return `/* eslint-disable */
// Generated by scripts/generate-shadcn-food-icons.mjs
// Source: ${FOOD_TAG_URL} (pages: ${FOOD_PAGE_COUNT})
// Generated at: ${generatedAt}

export const SHADCN_FOOD_ICONS = {
${iconArray}
} as const;

export type ShadcnFoodIconId = keyof typeof SHADCN_FOOD_ICONS;
`;
};

const toIconTokens = (value) =>
  value
    .toLowerCase()
    .split(/[^a-z0-9]+/g)
    .filter((token) => token.length > 1 && !ICON_STOP_TOKENS.has(token));

const buildCatalogOutput = (catalogEntries) => {
  const generatedAt = new Date().toISOString();
  const rows = catalogEntries
    .map((entry) => {
      const tokens = entry.name
        .split("-")
        .filter(Boolean)
        .map((token) => token.toLowerCase());
      return `  { id: ${JSON.stringify(entry.id)}, library: ${JSON.stringify(entry.library)}, name: ${JSON.stringify(entry.name)}, tokens: ${JSON.stringify(tokens)} }`;
    })
    .join(",\n");

  return `// Generated by scripts/generate-shadcn-food-icons.mjs
// Source: ${FOOD_TAG_URL} (pages: ${FOOD_PAGE_COUNT})
// Generated at: ${generatedAt}

export type ShadcnFoodIconCatalogEntry = {
  id: string;
  library: string;
  name: string;
  tokens: string[];
};

export const SHADCN_FOOD_ICON_CATALOG: ShadcnFoodIconCatalogEntry[] = [
${rows}
];
`;
};

const buildIngredientIndexOutput = (entries) => {
  const generatedAt = new Date().toISOString();
  const rows = entries
    .map((entry) => {
      const tokens = toIconTokens(entry.name);
      return `  { id: ${JSON.stringify(entry.id)}, library: ${JSON.stringify(entry.library)}, name: ${JSON.stringify(entry.name)}, tokens: ${JSON.stringify(tokens)} }`;
    })
    .join(",\n");

  return `// Generated by scripts/generate-shadcn-food-icons.mjs
// Source: ${FOOD_TAG_URL} (pages: ${FOOD_PAGE_COUNT})
// Generated at: ${generatedAt}

export type IngredientSemanticIconEntry = {
  id: string;
  library: string;
  name: string;
  tokens: string[];
};

export const INGREDIENT_SEMANTIC_ICON_INDEX: IngredientSemanticIconEntry[] = [
${rows}
];
`;
};

const shouldExcludeEntry = (entry) =>
  EXCLUDED_NAME_PATTERNS.some((pattern) => entry.name.includes(pattern));

const compareEntryPriority = (left, right) => {
  const leftPriority = LIBRARY_PRIORITY_INDEX.get(left.library);
  const rightPriority = LIBRARY_PRIORITY_INDEX.get(right.library);
  if (leftPriority == null && rightPriority == null) return 0;
  if (leftPriority == null) return 1;
  if (rightPriority == null) return -1;
  if (leftPriority !== rightPriority) return leftPriority - rightPriority;
  return left.id.localeCompare(right.id);
};

const selectIngredientSemanticIcons = (catalogEntries) => {
  const groupedByName = new Map();
  for (const entry of catalogEntries) {
    if (!LIBRARY_PRIORITY_INDEX.has(entry.library)) continue;
    if (shouldExcludeEntry(entry)) continue;
    if (!groupedByName.has(entry.name)) {
      groupedByName.set(entry.name, []);
    }
    groupedByName.get(entry.name).push(entry);
  }

  const selected = [];
  for (const matches of groupedByName.values()) {
    matches.sort(compareEntryPriority);
    selected.push(matches[0]);
  }

  return selected.sort((left, right) => left.id.localeCompare(right.id));
};

const run = async () => {
  const outputPath = process.argv[2] ? path.resolve(process.cwd(), process.argv[2]) : DEFAULT_OUTPUT;
  const catalogOutputPath = process.argv[3]
    ? path.resolve(process.cwd(), process.argv[3])
    : DEFAULT_CATALOG_OUTPUT;
  const ingredientIndexOutputPath = process.argv[4]
    ? path.resolve(process.cwd(), process.argv[4])
    : DEFAULT_INGREDIENT_INDEX_OUTPUT;

  const catalogById = new Map();
  for (let page = 1; page <= FOOD_PAGE_COUNT; page += 1) {
    const url = page === 1 ? FOOD_TAG_URL : `${FOOD_TAG_URL}?page=${page}`;
    const html = await fetchText(url);
    for (const entry of parseFoodIconIds(html)) {
      if (!catalogById.has(entry.id)) {
        catalogById.set(entry.id, entry);
      }
    }
  }

  const catalogEntries = [...catalogById.values()].sort((left, right) =>
    left.id.localeCompare(right.id)
  );
  const selectedIngredientIcons = selectIngredientSemanticIcons(catalogEntries);
  const iconIds = new Set(selectedIngredientIcons.map((entry) => entry.id));

  const sortedIds = [...iconIds].sort();
  const entries = [];
  for (const iconId of sortedIds) {
    const detailHtml = await fetchText(`${DETAIL_BASE}${iconId}`);
    const svg = extractInlineSvg(detailHtml);
    if (!svg) {
      console.warn(`warn: missing inline SVG for ${iconId}`);
      continue;
    }
    entries.push({ iconId, svg });

    // Avoid hammering the source site.
    await sleep(50);
  }

  const output = buildOutput(entries);
  await writeFile(outputPath, output, "utf8");
  await writeFile(catalogOutputPath, buildCatalogOutput(catalogEntries), "utf8");
  await writeFile(
    ingredientIndexOutputPath,
    buildIngredientIndexOutput(selectedIngredientIcons),
    "utf8"
  );

  console.log(`Generated ${entries.length} SVG icons -> ${outputPath}`);
  console.log(`Generated ${catalogEntries.length} icon catalog rows -> ${catalogOutputPath}`);
  console.log(
    `Generated ${selectedIngredientIcons.length} ingredient semantic icon rows -> ${ingredientIndexOutputPath}`
  );
};

run().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
